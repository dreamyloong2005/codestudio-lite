export function createZhTWDictionary<T extends Record<string, string>>(source: T): T {
  const converted = Object.fromEntries(
    Object.entries(source).map(([key, value]) => [key, toTraditional(value)])
  ) as T;

  return {
    ...converted,
    "settings.language.zhTW": "繁體中文"
  };
}

function toTraditional(value: string): string {
  let converted = value;
  for (const [from, to] of phraseMap) {
    converted = converted.replaceAll(from, to);
  }
  return Array.from(converted)
    .map((character) => charMap[character] ?? character)
    .join("");
}
